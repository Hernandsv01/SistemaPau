    public void setAnotaciones(){
        String info = "";
        
        //Set class info
        if(infoClase[6] != null){
            info += "Tem√°tica de las clase: \n";
            info += infoClase[6] + "\n \n";
        }
        
        //Set alumno info
        String statement2 = "SELECT * FROM `clasesalumnos` WHERE `id_clase` = '" + infoClase[0] + "'";
        List<String[]> alumnos = DBConnection.getInstance().selectStatement(statement2, 2);
        for(int i = 0; i < alumnos.size(); i++){
            String statement3 = "SELECT * FROM `comentariosalumnos` WHERE `dni_alumno` = '" + alumnos.get(i)[0] + "'";
            List<String[]> patologias = DBConnection.getInstance().selectStatement(statement3, 2);
            if(patologias.get(0)[1] != null){
                info += Alumno.getNameFromDNI(alumnos.get(i)[0]) + ": " + patologias.get(0)[1] + "\n";
            }
        }
        
        txtanotaciones.setText(info);
    }
    
